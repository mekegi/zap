package zap

// Option is used to set options for the logger.
type Option interface {
	apply(*logger)
}

// optionFunc wraps a func so it satisfies the Option interface.
type optionFunc func(*logger)

func (f optionFunc) apply(log *logger) {
	f(log)
}

// Fields sets the initial fields for the logger.
func Fields(fs ...Field) Option {
	return optionFunc(func(l *logger) {
		l.fac = l.fac.With(fs)
	})
}

// ErrorOutput sets the destination for errors generated by the logger. The
// supplied WriteSyncer is automatically wrapped with a mutex, so it need not be
// safe for concurrent use.
func ErrorOutput(w WriteSyncer) Option {
	return optionFunc(func(log *logger) {
		log.errorOutput = newLockedWriteSyncer(w)
	})
}

// Development puts the logger in development mode, which alters the behavior
// of the DPanic method.
func Development() Option {
	return optionFunc(func(log *logger) {
		log.development = true
	})
}

// AddCaller configures the Logger to annotate each entry with the filename
// and line number of zap's caller.
func AddCaller() Option {
	return AddCallerSkip(0)
}

// AddCallerSkip configures the Logger to annotate each message with the filename
// and line number of a caller outside of zap; which caller in the stack is
// controlled by the skip argument. If more than one AddCallerSkip option is
// provided, then the maximum skip amount is used.
func AddCallerSkip(skip int) Option {
	return optionFunc(func(log *logger) {
		log.addCaller = true
		if n := skip + _defaultCallerSkip; n > log.callerSkip {
			log.callerSkip = n
		}
	})
}

// AddStacks configures the Logger to record a stack trace for all messages at
// or above a given level. Keep in mind that this is (relatively speaking)
// quite expensive.
//
// TODO: why is this called AddStacks rather than just AddStack or
// AddStacktrace?
func AddStacks(lvl Level) Option {
	return optionFunc(func(log *logger) {
		log.addStack = lvl
	})
}
